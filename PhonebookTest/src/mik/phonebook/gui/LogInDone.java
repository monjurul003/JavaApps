    public void loadTableData() {

        DefaultTableModel dtmodel = (DefaultTableModel) this.jTable1.getModel();

        dtmodel.setRowCount(0);
        for (int i = 0; i < this.conList.size(); i++) {
            dtmodel.addRow(new Object[]{this.conList.get(i).getName(), this.conList.get(i).getNumber()});
        }

    }


