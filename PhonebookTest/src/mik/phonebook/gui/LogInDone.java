    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        // TODO add your handling code here:
        if (this.i >= 0 && this.btnAdd.getText().equalsIgnoreCase("Update")) {
            this.conList.remove(this.i);
            fp.saveContactListInFile(this.userName, this.conList);
            this.i = -1;
            this.loadTableData();
        }
        this.txtName.setText("");
        this.txtNumber.setText("");
        this.btnAdd.setText("Add");
        this.btnClear.setText("Clear");
    }//GEN-LAST:event_btnClearActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String name = this.txtName.getText().toString();
        String number = this.txtNumber.getText().toString();

        if (this.i >= 0 && this.btnAdd.getText().equalsIgnoreCase("Update")) {
            if (!name.equalsIgnoreCase(this.conList.get(this.i).getName()) || !number.equalsIgnoreCase(this.conList.get(this.i).getNumber())) {
                this.conList.get(this.i).setName(name);
                this.conList.get(this.i).setNumber(number);
                fp.saveContactListInFile(this.userName, this.conList);
                this.loadTableData();
                this.i = -1;
            }
        } else {

            boolean isExist = false;
            Contact obj = new Contact(name.trim(), number.trim());
            for (int i = 0; i < this.conList.size(); i++) {
                if (name.equalsIgnoreCase(this.conList.get(i).getName()) || number.equals(this.conList.get(i).getNumber())) {
                    JOptionPane.showMessageDialog(rootPane, "The contact is already in your phonebook!");
                    isExist = true;
                }
            }
            if (!isExist) {
                this.conList.add(obj);

                fp.saveContactListInFile(this.userName, this.conList);
                this.loadTableData();
            }
        }
        this.txtName.setText("");
        this.txtNumber.setText("");
        this.btnAdd.setText("Add");
        this.btnClear.setText("Clear");

    }//GEN-LAST:event_btnAddActionPerformed

