public class Calculator {
    private int a,b;

    public Calculator() {
        a=0;
        b=0;
    }

    public Calculator(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
    public int add(){
        return (a+b);
    }
}
